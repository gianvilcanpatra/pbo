    private void getTotalBayar(){
        int totBayar=0;
        if(menuSpa.isSelected()){
            totBayar += 40000;
        }
        if(menuSteak.isSelected()){
            totBayar += 50000;
        }
        if(menuPizza.isSelected()){
            totBayar += 80000;
        }
        totalBayar.setText(String.valueOf(totBayar));
    }

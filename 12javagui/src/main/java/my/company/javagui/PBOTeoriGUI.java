    private void loadTeksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadTeksActionPerformed
        // TODO add your handling code here:
        try{
            File filePesanan = new File("C:\\Users\\Gian Vilcan Patra\\OneDrive\\Documents\\NetBeansProjects\\12javagui\\src\\main\\java\\my\\company\\javagui\\pesanan.txt");
            FileReader fr = new FileReader(filePesanan);
            BufferedReader rd = new BufferedReader(fr);
            
            dataPenjualan.setText("");
            String line = null;
            while((line = rd.readLine()) != null){
                dataPenjualan.setText(dataPenjualan.getText()+line+"\n");
            }
            rd.close();
        } catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_loadTeksActionPerformed

